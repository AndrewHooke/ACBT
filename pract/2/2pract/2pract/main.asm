ORG 0000H

; SFR ????????
P0 EQU 080H
P1 EQU 090H
P2 EQU 0A0H
P3 EQU 0B0H

; ??? P3.1
BUTTON BIT 0B1H

MAIN:
    MOV P2, #0FFH
    MOV R0, #9

DISPLAY_LOOP:
    MOV A, R0
    MOV DPTR, #SEGMENT_CODES
    MOVC A, @A+DPTR
    MOV P2, A

WAIT_PRESS:
    JB BUTTON, WAIT_PRESS

DEBOUNCE:
    MOV R7, #0FFH

DEBOUNCE_LOOP:
    DJNZ R7, DEBOUNCE_LOOP

WAIT_RELEASE:
    JNB BUTTON, WAIT_RELEASE

DECREMENT:
    DEC R0
    CJNE R0, #0FFH, DISPLAY_LOOP

    MOV R0, #9
    SJMP DISPLAY_LOOP

SEGMENT_CODES:
    DB 0C0H ; 0
    DB 0F9H ; 1
    DB 0A4H ; 2
    DB 0B0H ; 3
    DB 099H ; 4
    DB 092H ; 5
    DB 082H ; 6
    DB 0F8H ; 7
    DB 080H ; 8
    DB 090H ; 9

END