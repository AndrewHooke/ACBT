$MOD51

AJMP START

WORD:   DB 169, 178, 180, 176, 175, 162
LEN     EQU 06H

START:
    ACALL LCD_INIT

MAIN_LOOP:
    MOV A, #080H
    ACALL LCD_CMD
    ACALL PRINT_WORD
    ACALL DELAY_LONG
    ACALL LCD_CLEAR

    MOV A, #082H
    ACALL LCD_CMD
    ACALL PRINT_WORD
    ACALL DELAY_LONG
    ACALL LCD_CLEAR

    MOV A, #0C0H
    ACALL LCD_CMD
    ACALL PRINT_WORD
    ACALL DELAY_LONG
    ACALL LCD_CLEAR

    MOV A, #0C2H
    ACALL LCD_CMD
    ACALL PRINT_WORD
    ACALL DELAY_LONG
    ACALL LCD_CLEAR

    SJMP MAIN_LOOP

LCD_INIT:
    MOV A, #038H
    ACALL LCD_CMD
    ACALL DELAY_SHORT

    MOV A, #00EH
    ACALL LCD_CMD
    ACALL DELAY_SHORT

    MOV A, #001H
    ACALL LCD_CMD
    ACALL DELAY_SHORT

    MOV A, #080H
    ACALL LCD_CMD
    ACALL DELAY_SHORT
    RET

LCD_CLEAR:
    MOV A, #001H
    ACALL LCD_CMD
    ACALL DELAY_SHORT
    RET

LCD_CMD:
    MOV P0, A
    MOV P2, #01H
    ACALL DELAY_E
    MOV P2, #00H
    ACALL DELAY_E
    RET

LCD_DATA:
    MOV P0, A
    MOV P2, #03H
    ACALL DELAY_E
    MOV P2, #02H
    ACALL DELAY_E
    RET

PRINT_WORD:
    MOV DPTR, #WORD
    MOV R7, #06H

PRINT_LOOP:
    CLR A
    MOVC A, @A+DPTR
    ACALL LCD_DATA
    INC DPTR
    DJNZ R7, PRINT_LOOP
    RET

DELAY_E:
    NOP
    NOP
    NOP
    RET

DELAY_SHORT:
    MOV R5, #4
DS1:
    MOV R6, #50
DS2:
    DJNZ R6, DS2
    DJNZ R5, DS1
    RET

DELAY_LONG:
    MOV R3, #4
DL1:
    ACALL DELAY_SHORT
    DJNZ R3, DL1
    RET

END