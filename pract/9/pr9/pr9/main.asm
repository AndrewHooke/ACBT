
$MOD51

ORG 0000H
    LJMP START

ORG 0030H

START:
    MOV TMOD, #00010000B

MAIN:
    MOV R1, #50
ETAP1_LOOP:
    MOV P2, #00H
    ACALL DELAY_5MS
    DJNZ R1, ETAP1_LOOP

    MOV P2, #80

    MOV R0, #80
    MOV R1, #80
ETAP3_LOOP:
    MOV P2, R0
    ACALL DELAY_3_125MS
    INC R0
    DJNZ R1, ETAP3_LOOP

    MOV P2, #80

    MOV R1, #50
ETAP5_LOOP:
    MOV P2, #80
    ACALL DELAY_5MS
    DJNZ R1, ETAP5_LOOP

    MOV P2, #160

    MOV R0, #160
    MOV R1, #40
ETAP7_LOOP:
    MOV P2, R0
    ACALL DELAY_6_25MS    ; ???? 5 ??, ????? 6.25 ?? (250 ?? / 40 ?????)
    DEC R0
    DJNZ R1, ETAP7_LOOP

    MOV P2, #00H

    MOV R1, #50
ETAP9_LOOP:
    MOV P2, #00H
    ACALL DELAY_5MS
    DJNZ R1, ETAP9_LOOP

    SJMP MAIN

DELAY_5MS:
    MOV TH1, #0ECH
    MOV TL1, #078H
    SETB TR1
WAIT_5MS:
    JNB TF1, WAIT_5MS
    CLR TR1
    CLR TF1
    RET

DELAY_6_25MS:
    MOV TH1, #0E7H        ; 65536 - 6250 = 59286 = E796H
    MOV TL1, #096H
    SETB TR1
WAIT_6_25MS:
    JNB TF1, WAIT_6_25MS
    CLR TR1
    CLR TF1
    RET

DELAY_3_125MS:
    MOV TH1, #0F3H
    MOV TL1, #0CBH
    SETB TR1
WAIT_3_125MS:
    JNB TF1, WAIT_3_125MS
    CLR TR1
    CLR TF1
    RET

END